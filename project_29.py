# **Topic:** Tuples

# **Task:**
# Unpack a tuple of three elements into separate variables.

# **Hints:**

# * Use `a,b,c = tuple`

# **Example expected output:**

# ```
# Tuple: (1,2,3)
# a=1, b=2, c=3
